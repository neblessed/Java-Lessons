public class Man {
    private String name;
    private String job;
    private int age;
    private double height;

    public Man(String name, String job, int age, double height) {
        this.name = name;
        this.job = job;
        this.age = age;
        this.height = height;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }

    public int getAge() {
        return age;
    }

    public double getHeight() {
        return height;
    }
}
